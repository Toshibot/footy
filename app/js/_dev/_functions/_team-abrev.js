function teamAbrev(array) {
   var team = array;

   if (team == 'ADE') {
      return 'Adelaide'

   } else if (team == 'BRI') {
      return 'Brisbane'

   } else if (team == 'CAR') {
      return 'Carlton'

   } else if (team == 'COL') {
      return 'Collingwood'

   } else if (team == 'ESS') {
      return 'Essendon'

   } else if (team == 'WBD') {
      return 'W. Bulldogs'

   } else if (team == 'FRE') {
      return 'Fremantle'

   } else if (team == 'GEE') {
      return 'Geelong'

   } else if (team == 'GCS') {
      return 'Gold Coast'

   } else if (team == 'GWS') {
      return 'W. Sydney'

   } else if (team == 'HAW') {
      return 'Hawthorn'

   } else if (team == 'MEL') {
      return 'Melbourne'

   } else if (team == 'NM') {
      return 'N. Melbourne'

   } else if (team == 'POR') {
      return 'Port Adelaide'

   } else if (team == 'RIC') {
      return 'Richmond'

   } else if (team == 'STK') {
      return 'St. Kilda'

   } else if (team == 'SYD') {
      return 'Sydney'

   } else if (team == 'WCE') {
      return 'West Coast'

   } else {
      return team
   }
}